package exceptions;

/**
 * @author cantleyc
 *
 */
public class GivenNameInvalidException extends Exception {
	private static final long serialVersionUID = -5660174830670926590L;

	public GivenNameInvalidException() {
	}

	/**
	 * @param message
	 */
	public GivenNameInvalidException(String message) {
		super(message);		
	}
	
	/**
	 * @param cause
	 */
	public GivenNameInvalidException(Throwable cause) {
		super(cause);
	}
	
	/**
	 * @param message
	 * @param cause
	 */
	public GivenNameInvalidException(String message, Throwable cause) {
		super(message, cause);
	}	
}
