package exceptions;

/**
 * @author 432873
 *
 */
public class NumberInvalidException extends Exception {
	private static final long serialVersionUID = -7176960923827395397L;
	
	/**
	 * 
	 */
	public NumberInvalidException()
	{
		
	}

	/**
	 * @param message
	 */
	public NumberInvalidException(String message)
	{
		super(message);
	}
	
	/**
	 * @param cause
	 */
	public NumberInvalidException(Throwable cause)
	{
		super(cause);
	}
	
	/**
	 * @param message
	 * @param cause
	 */
	public NumberInvalidException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
