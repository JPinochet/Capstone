public class TearDownTimeInvalidException extends Exception 
{
	private static final long serialVersionUID = -8433414654149300188L;

	/**
	 * 
	 */
	public TearDownTimeInvalidException()
	{
		
	}
	
	/**
	 * @param message
	 */
	public TearDownTimeInvalidException(String message)
	{
		super(message);
	}
	
	/**
	 * @param cause
	 */
	public TearDownTimeInvalidException(Throwable cause)
	{
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public TearDownTimeInvalidException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
