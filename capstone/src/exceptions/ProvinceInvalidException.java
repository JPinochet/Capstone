package exceptions;

/**
 * @author 432873
 *
 */
public class ProvinceInvalidException extends Exception {
	private static final long serialVersionUID = 4551801712884851415L;

	/**
	 * 
	 */
	public ProvinceInvalidException()
	{
		
	}
	
	/**
	 * @param message
	 */
	public ProvinceInvalidException(String message)
	{
		super(message);
	}
	
	/**
	 * @param cause
	 */
	public ProvinceInvalidException(Throwable cause)
	{
		super(cause);
	}
	
	/**
	 * @param message
	 * @param cause
	 */
	public ProvinceInvalidException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
