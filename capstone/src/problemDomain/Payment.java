package problemDomain;

import java.util.Date;

/**
 * @author Corey Cantley
 * @version 1.0
 */
public class Payment {
	private int id;
	private Date date;
	private double amount;

	/**
	 * @param id
	 * @param date
	 * @param amount
	 */
	public Payment(int id, Date date, double amount) {
		this.id = id;
		this.date = date;
		this.amount = amount;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @return the date
	 */
	public Date getDate() {
		return date;
	}

	/**
	 * @return the amount
	 */
	public double getAmount() {
		return amount;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @param date
	 *            the date to set
	 */
	public void setDate(Date date) {
		this.date = date;
	}

	/**
	 * @param amount
	 *            the amount to set
	 */
	public void setAmount(double amount) {
		this.amount = amount;
	}
}
