package exceptions;

/**
 * @author 432873
 *
 */
public class DamageDepositInvalidException extends Exception {
	private static final long serialVersionUID = 7419363301866827747L;

	/**
	 * Initializes Exception
	 */
	public DamageDepositInvalidException()
	{
		
	}
	
	/**
	 * Gets the specific message for the exception
	 * @param message is the message that is thrown for the exception
	 */
	public DamageDepositInvalidException(String message)
	{
		super(message);
	} 
	
	/**
	 * Gets the specific cause for the exception
	 * @param cause is the cause for the reason an exception is thrown
	 */
	public DamageDepositInvalidException(Throwable cause)
	{
		super(cause);
	}
	
	/**
	 * Gets the specific message and cause for the exception that is thrown
	 * @param message is the message that is thrown for the exception
	 * @param cause is the cause for the reason an exception is thrown
	 */
	public DamageDepositInvalidException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
