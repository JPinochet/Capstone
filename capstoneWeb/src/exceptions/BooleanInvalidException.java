package exceptions;

/**
 * @author 432873
 *
 */
public class BooleanInvalidException extends Exception 
{
	private static final long serialVersionUID = 6995115240498451863L;

	/**
	 * Initializes Exception
	 */
	public BooleanInvalidException()
	{
		
	}
	
	/**
	 * Gets the specific message for the exception
	 * @param message is the message that is thrown for the exception
	 */
	public BooleanInvalidException(String message)
	{
		super(message);
	}
	
	/**
	 * Gets the specific cause for the exception
	 * @param cause is the cause for the reason an exception is thrown 
	 */
	public BooleanInvalidException(Throwable cause)
	{
		super(cause);
	}
	
	/**
	 * Gets the specific message and cause for the exception that is thrown
	 * @param message is the message that is thrown for the exception
	 * @param cause is the cause for the reason an exception is thrown
	 */
	public BooleanInvalidException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
